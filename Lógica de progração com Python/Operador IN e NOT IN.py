# in = entre.
# not in = não está entre.

nome = 'Otávio'

print('á' in nome) # o in serve para se a letra está dentro nome. E vai retornar true ou false.
print('vio' in nome)
print(10*'-')
print('á' not in nome)
print('vio' not in nome) # not in faz o inverso do in. true=false e vice e versa.

